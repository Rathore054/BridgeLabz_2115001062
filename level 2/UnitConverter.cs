using System;

public class UnitConverter
{
    public static void Main()
    {
        Console.WriteLine("10 km to miles: " + (10 * 0.621371));
        Console.WriteLine("6.2 miles to km: " + (6.2 * 1.60934));
        Console.WriteLine("100 meters to feet: " + (100 * 3.28084));
        Console.WriteLine("328 feet to meters: " + (328 * 0.3048));
    }
}
