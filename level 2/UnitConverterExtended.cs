using System;

public class UnitConverterExtended
{
    public static void Main()
    {
        Console.WriteLine("5 yards to feet: " + (5 * 3));
        Console.WriteLine("15 feet to yards: " + (15 * 0.333333));
        Console.WriteLine("2 meters to inches: " + (2 * 39.3701));
        Console.WriteLine("78 inches to meters: " + (78 * 0.0254));
        Console.WriteLine("12 inches to cm: " + (12 * 2.54));
    }
}
